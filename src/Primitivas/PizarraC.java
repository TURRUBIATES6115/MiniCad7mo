    public void lineFast(int x0, int y0, int x1, int y1, Color color) {
        int pix = color.getRGB();
        int dy = y1 - y0;
        int dx = x1 - x0;
        int stepx, stepy;
        if (dy < 0) {
            dy = -dy;
            stepy = -raster.width;
        } else {
            stepy = raster.width;
        }
        if (dx < 0) {
            dx = -dx;
            stepx = -1;
        } else {
            stepx = 1;
        }
        dy <<= 1;
        dx <<= 1;
        y0 *= raster.width;
        y1 *= raster.width;
        raster.pixel[x0 + y0] = pix;
        if (dx > dy) {
            int fraction = dy - (dx >> 1);
            while (x0 != x1) {
                if (fraction >= 0) {
                    y0 += stepy;
                    fraction -= dx;
                }
                x0 += stepx;
                fraction += dy;
                raster.pixel[x0 + y0] = pix;
            }
        } else {
            int fraction = dx - (dy >> 1);
            while (y0 != y1) {
                if (fraction >= 0) {
                    x0 += stepx;
                    fraction -= dy;
                }
                y0 += stepy;
                fraction += dx;
                raster.pixel[x0 + y0] = pix;
            }
        }
    }

    void PlotPoint(Graphics g, int xc, int yc, int x, int y) {
        g.setColor(Color.red);
        g.drawRect(xc + x, yc + y, 1, 1);
        g.drawRect(xc - x, yc + y, 1, 1);
        g.drawRect(xc + x, yc - y, 1, 1);
        g.drawRect(xc - x, yc - y, 1, 1);
        g.drawRect(xc + y, yc + x, 1, 1);
        g.drawRect(xc - y, yc + x, 1, 1);
        g.drawRect(xc + y, yc - x, 1, 1);
        g.drawRect(xc - y, yc - x, 1, 1);
    }

    void CircleMidPoint(Point p1, Point p2) {
        int x, y, p, xc = p1.x, yc = p1.y;
        int r = (int) Math.sqrt(((p2.x - p1.x) * (p2.x - p1.x)) + ((p2.y - p1.y) * (p2.y - p1.y)));
        Graphics g = this.getGraphics();
        x = 0;
        y = r;
        p = 1 - r;
        PlotPoint(g, xc, yc, x, y);
        while (x < y) {
            x = x + 1;
            if (p < 0) {
                p = p + 2 * x + 1;
            } else {
                y = y - 1;
                p = p + 2 * (x - y) + 1;
            }
            PlotPoint(g, xc, yc, x, y);
        }
    }

    public void Elipse2(Point p1, Point p2) {
        Graphics g = this.getGraphics();
        g.setColor(Color.red);
        int an = (int) Math.sqrt(((p2.x - p1.x) * (p2.x - p1.x)) + ((p2.y - p1.y) * (p2.y - p1.y)));
        g.drawOval(p1.x, p1.y, an * 2, an);
    }

    public void triangulo(Point p1, Point p2, Point p3) {
        lineFast(p1.x, p1.y, p2.x, p2.y, Color.red);
        lineFast(p1.x, p1.y, p3.x, p3.y, Color.red);
        lineFast(p2.x, p2.y, p3.x, p3.y, Color.red);
        Graphics g = this.getGraphics();
        g.setColor(Color.red);
        int x[] = {p1.x, p2.x, p3.x};
        int y[] = {p1.y, p2.y, p3.y};
        g.fillPolygon(x, y, 3);
    }

