    private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
        // TODO add your handling code here:
        switch (op) {
            case 1:
                if (!bDibujar) {
                    p1.x = evt.getX();
                    p1.y = evt.getY();
                    bDibujar = true;
                } else {
                    p2.x = evt.getX();
                    p2.y = evt.getY();
                    dibujarLinea(p1, p2, Color.red);
                    bDibujar = false;
                }   break;
            case 2:
                if (!bDibujar) {
                    p1.x = evt.getX();
                    p1.y = evt.getY();
                    bDibujar = true;
                } else {
                    p2.x = evt.getX();
                    p2.y = evt.getY();
                    CircleMidPoint(p1, p2);
                    bDibujar = false;
                }   break;
            case 3:
                if (!bDibujar) {
                    p1.x = evt.getX();
                    p1.y = evt.getY();
                    bDibujar = true;
                } else {
                    p2.x = evt.getX();
                    p2.y = evt.getY();
                    Elipse2(p1, p2);
                    bDibujar = false;
                }   break;
            case 4:
                if (!bDibujar && !bDibujar2) {
                    p1.x = evt.getX();
                    p1.y = evt.getY();
                    bDibujar = true;
                } else if (bDibujar && !bDibujar2) {
                    p2.x = evt.getX();
                    p2.y = evt.getY();
                    bDibujar2 = true;
                } else if (bDibujar && bDibujar2) {
                    p3.x = evt.getX();
                    p3.y = evt.getY();
                    triangulo(p1, p2, p3);
                    bDibujar = bDibujar2 = false;
                }   break;
            default:
                break;
        }
    }//GEN-LAST:event_formMouseClicked

